    public void listar() {
        VendasDAO dao = new VendasDAO();
        List<Vendas> lista = dao.listarVendas();
        DefaultTableModel dados = (DefaultTableModel) tabelaCliente.getModel();
        dados.setNumRows(0);
        for (Vendas c : lista) {
            dados.addRow(new Object[]{
                c.getId(),
                c.getData_venda(),
                c.getTotal_venda(),
                c.getCliente_id(),
                c.getFuncionario_id(),
                c.getCliente_nome(),
                c.getProduto_nome()

            });

        }
    }

