    private void tfPesquisarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfPesquisarKeyPressed
        try {
            String nome = "%" + tfPesquisar.getText() + "%";
            ClientesDAO dao = new ClientesDAO();
            List<Cliente> lista = dao.buscarClientesPorNome(nome);
            DefaultTableModel dados = (DefaultTableModel) tabelaCliente.getModel();
            dados.setNumRows(0);
            for (Cliente c : lista) {
                dados.addRow(new Object[]{
                    c.getId(),
                    c.getNome(),
                    c.getRg(),
                    c.getCpf(),
                    c.getEndereco(),
                    c.getCidade(),
                    c.getUf(),
                    c.getCep(),
                    c.getBairro(),
                    c.getNumero()
                });

            }
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Erro no formulario pesquisar" + e);
        }
    }//GEN-LAST:event_tfPesquisarKeyPressed

